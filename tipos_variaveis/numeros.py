# Inteiro
numero_inteiro = 3
print("Inteiro = ", numero_inteiro)

# Real com ponto flutuante
numero_real = 2.3
print("Real com ponto flutuante = ", numero_real)

# type() obj retornar o tipo de variavel que esta trabalhando
print("Tipo de variavel inteiro", type(numero_inteiro))
print("Real com ponto flutuante", type(numero_real))

# soma +
soma = 1 + 1
print("1 + 1 ", soma)

# subtracao
subtracao = 1 - 1
print("1 - 1 ", subtracao)

# multiplicacao
multplicacao = 2 * 2
print("2 x 2 ", multplicacao)

# divisao
divisao = 5 / 2
print("5 / 2 ", divisao)
print("Tipo do resultado da divisao", type(divisao))

# divisao inteiro usando //
divisao = 5 // 2
print("5 // 2 = resultado com numero inteiro ", divisao)
print("Tipo do resultado da divisao", type(divisao))

# mudar variavel de float par ainteiro usando o int()
print("Valor em inteiro", int(divisao))

# mudar variavel de int para inteiro usando o float()
print("Valor em float", float(soma))

# modulo e o resto da divisao
modulo = 5 % 2
print("Resto da divisao", modulo)
